import torch.nn.functional as F
from einops import rearrange

# Helper functions for window partitioning/reversing (assuming these are defined)
def window_partition(x, window_size):
    B, C, H, W = x.shape
    x = x.view(B, C, H // window_size, window_size, W // window_size, window_size)
    windows = x.permute(0, 2, 4, 3, 5, 1).contiguous().view(-1, window_size * window_size, C)  # flatten windows
    return windows

def window_reverse(windows, window_size, H, W):
    B = int(windows.shape[0] / (H * W / window_size / window_size))
    x = windows.view(B, H // window_size, W // window_size, window_size, window_size, -1)
    x = x.permute(0, 5, 1, 3, 2, 4).contiguous().view(B, -1, H, W)
    return x

class FFTWindowAttention(nn.Module):
    def __init__(self, dim, window_size, num_heads, bias):
        super(FFTWindowAttention, self).__init__()
        self.dim = dim
        self.window_size = window_size
        self.num_heads = num_heads

        # Inspired by FSAS
        self.to_hidden = nn.Conv2d(dim, dim * 6, kernel_size=1, bias=bias)
        self.to_hidden_dw = nn.Conv2d(dim * 6, dim * 6, kernel_size=3, stride=1, padding=1, groups=dim * 6, bias=bias)
        self.project_out = nn.Conv2d(dim * 2, dim, kernel_size=1, bias=bias)

        # LayerNorm for normalization after FFT, assuming dim*2 channels
        self.norm = nn.LayerNorm(dim * 2)

    def forward(self, x, H, W):
        # x: (B, C, H, W)
        B, C, H_in, W_in = x.shape
        assert H == H_in and W == W_in, "Input feature size mismatch"

        # Partition windows
        x_windows = window_partition(x, self.window_size)  # (num_windows*B, window_size*window_size, C)

        # Reshape windows back to (B*num_windows, C, window_size, window_size)
        num_windows = x_windows.shape[0]
        x_windows_reshaped = x_windows.view(num_windows, self.window_size, self.window_size, C).permute(0, 3, 1, 2).contiguous()

        # FSAS style operations
        hidden = self.to_hidden(x_windows_reshaped)  # (num_windows*B, 6*C, ws, ws)
        q, k, v = self.to_hidden_dw(hidden).chunk(3, dim=1)  # each (num_windows*B, 2*C, ws, ws)

        # Reshape to patches for FFT
        patch_size = self.window_size  # using window size as patch size here
        q_patch = rearrange(q, 'b c h w -> b c h w')  # no patch reshaping needed, already window sized
        k_patch = rearrange(k, 'b c h w -> b c h w')

        # FFT in spatial dims
        q_fft = torch.fft.rfft2(q_patch.float())
        k_fft = torch.fft.rfft2(k_patch.float())

        # Element-wise multiplication (correlation)
        out = q_fft * k_fft

        # Inverse FFT back
        out = torch.fft.irfft2(out, s=(patch_size, patch_size))

        # Normalize and combine
        out = out.permute(0, 2, 3, 1).contiguous()  # (B*num_windows, ws, ws, 2C)
        out = self.norm(out)
        out = out.permute(0, 3, 1, 2).contiguous()  # (B*num_windows, 2C, ws, ws)

        # Output: multiply with v and project out
        output = v * out
        output = self.project_out(output)  # (num_windows*B, C, ws, ws)

        # Flatten windows back and reverse partition
        output = output.permute(0, 2, 3, 1).contiguous().view(num_windows, self.window_size * self.window_size, C)
        out_windows = window_reverse(output, self.window_size, H, W)  # (B, C, H, W)

        return out_windows


class TransformerBlock(nn.Module):
    def __init__(self, dim, num_heads, ffn_expansion_factor, bias, LayerNorm_type, window_size=8):
        super(TransformerBlock, self).__init__()

        self.norm1 = LayerNorm(dim, LayerNorm_type)
        self.attn = FFTWindowAttention(dim, window_size, num_heads, bias = False)
        self.norm2 = LayerNorm(dim, LayerNorm_type)
        self.ffn = FeedForward(dim, ffn_expansion_factor, bias)

    def forward(self, x):
        B, C, H, W = x.shape
        x = x + self.attn(self.norm1(x) ,  H, W)
        x = x + self.ffn(self.norm2(x))

        return x

